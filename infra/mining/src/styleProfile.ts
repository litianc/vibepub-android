export const LITIAN_C_WECHAT_STYLE_PROFILE = `
【litianc 写作风格画像】
1. 整体气质：真实、理性、结构化、有个人判断，但不浮夸。
2. 使用第一人称视角，保留“我”的观察和判断，但不要过度抒情。
3. 从真实问题、具体观察或一次亲身排查开场，不写空泛鸡汤式引言。
4. 开头尽早给出核心判断：这篇文章到底想说明什么。
5. 优先使用“问题背景 -> 关键判断 -> 分层拆解 -> 证据/案例 -> 建议 -> 总结”的结构。
6. 标题要有明确对象和判断，避免夸张标题党；可以使用“对象 + 核心判断”“场景化提醒”“一次排查/实战引发的拆解”等形态。
7. 正文段落保持短而清楚，每段只承载一个意思，避免长段堆叠。
8. 多使用小标题、编号列表、表格来降低阅读成本。
9. 可以使用类比，但类比必须服务于解释复杂系统，不能只做修辞。
10. 技术内容要保留关键参数、版本、链路、失败条件和排查过程。
11. 产品/组织内容要把抽象判断落到具体机制、场景和下一步动作。
12. 结尾要回到可执行建议或清晰判断，不写泛泛的励志收束。

【公众号排版要求】
1. content 必须是适合微信公众号草稿的 HTML 片段，不要包含完整 html/body/head。
2. 只使用微信公众号兼容的基础标签：section、p、strong、em、blockquote、ul、ol、li、table、thead、tbody、tr、th、td、hr、code、pre。
3. 样式尽量使用 inline style；整体克制，适合长文阅读。
4. 正文段落建议使用 16px 字号、1.75-1.9 行高、适当段前段后留白。
5. 小标题要清晰承担逻辑推进，可以使用编号标题，但不要过度装饰。
6. 重点句可以单独成段并加粗，不要全篇大量加粗。
7. 如果原始材料里存在对比、测试、版本、方案优缺点，优先整理成表格。
8. 不要编造原始录音中没有出现的事实、数据、案例、引用或参考资料。
9. 如果信息不足，用稳妥的表达保留不确定性，不要强行写成定论。
10. 生成 imagePrompt 时，用英文描述一张解释性封面图或概念图，避免文字、logo、水印、暗黑氛围和纯抽象渐变。
`.trim();

export function buildWechatArticlePrompt(rawText: string): string {
  return `你是一个顶级的个人文字助理，擅长“风格蒸馏（Style Distillation）”。
我通过录音口述了一段想法，以下是语音转文字的原始草稿。
请你把它整理成一篇结构清晰、逻辑连贯、适合发布在微信公众号的文章。

${LITIAN_C_WECHAT_STYLE_PROFILE}

【输出要求】
请只返回 JSON 对象，不要返回 Markdown 代码块或额外说明。
JSON 必须包含：
- title：文章标题，保持克制、有对象、有判断。
- content：正文 HTML 片段，遵守上面的公众号排版要求。
- imagePrompt：英文 AI 绘图提示词，用于生成公众号封面图，画面要能解释文章核心概念，不要包含文字。

【原始录音文本】
${rawText.trim()}`;
}
